def _migrate_task(task: dict[str, Any]) -> dict[str, Any]:
    """Apply all migrations to a single task dict. Mutates and returns it."""
    return _ensure_deps_normalized(task)


def _migrate_tasks(tasks: list[dict[str, Any]]) -> list[dict[str, Any]]:
    """Apply all migrations to every task in a list."""
    for t in tasks:
        _migrate_task(t)
    return tasks


def load_tasks_yaml() -> tuple[dict[str, Any], list[dict[str, Any]]]:
    """Load tasks.yaml. Returns (meta, tasks_list).

    If file does not exist, raises FileNotFoundError.
    If YAML is malformed, raises yaml.YAMLError.
    If file exists but is empty or has unexpected structure, returns defaults.

    Auto-migrates legacy 'related' fields to 'deps' list and named priorities
    to numeric on load.
    """
    path = _tasks_file()
    if not path.exists():
        raise FileNotFoundError(f"Tasks file not found: {path}\nRun 'tasks init' first.")

    raw = yaml.safe_load(path.read_text(encoding="utf-8")) or {}
    meta = raw.get("meta", {})
    if not isinstance(meta, dict):
        meta = {}
    tasks = raw.get("tasks", [])
    if not isinstance(tasks, list):
        tasks = []
    _migrate_tasks(tasks)
    return meta, tasks


def save_tasks_yaml(meta: dict[str, Any], tasks: list[dict[str, Any]]) -> None:
    """Write tasks.yaml with meta and tasks list.

    Tasks are stored newest-first (by 'created' descending).
    Missing optional fields are excluded from output.
    """
    path = _tasks_file()
    path.parent.mkdir(parents=True, exist_ok=True)
    data: dict[str, Any] = {"meta": meta, "tasks": _strip_none_fields_from_list(tasks)}
    content = TASKS_HEADER + yaml.safe_dump(data, default_flow_style=False, sort_keys=False,
                                             allow_unicode=True)
    path.write_text(content, encoding="utf-8")


def load_closed_yaml() -> list[dict[str, Any]]:
    """Load closed.yaml. Returns list of closed tasks.

    Handles the {meta, tasks} format.
    Falls back to legacy done.yaml if closed.yaml doesn't exist.
    Returns [] if neither file exists.

    Auto-migrates legacy fields on load.
    """
    path = _resolve_tasks_dir() / CLOSED_FILE_NAME
    if not path.exists():
        old_path = _resolve_tasks_dir() / "done.yaml"
        if old_path.exists():
            raw = yaml.safe_load(old_path.read_text(encoding="utf-8")) or []
            if isinstance(raw, dict):
                tasks = raw.get("tasks", [])
            else:
                tasks = raw if isinstance(raw, list) else []
            _migrate_tasks(tasks)
            return tasks
        return []
    raw = yaml.safe_load(path.read_text(encoding="utf-8")) or []
    if isinstance(raw, dict):
        tasks = raw.get("tasks", [])
    else:
        tasks = raw if isinstance(raw, list) else []
    _migrate_tasks(tasks)
    return tasks


def save_closed_yaml(closed_list: list[dict[str, Any]]) -> None:
    """Write closed.yaml with meta wrapper and header.

    Oldest-first (chronological order). Meta tracks total_closed counter.
    """
    path = _resolve_tasks_dir() / CLOSED_FILE_NAME
    path.parent.mkdir(parents=True, exist_ok=True)
    meta = {"total_closed": 0}
    if path.exists():
        existing_raw = yaml.safe_load(path.read_text(encoding="utf-8")) or {}
        if isinstance(existing_raw, dict):
            meta = existing_raw.get("meta", meta)
    meta["total_closed"] = len(closed_list)
    data = {"meta": meta, "tasks": _strip_none_fields_from_list(closed_list)}
    content = CLOSED_HEADER + yaml.safe_dump(data, default_flow_style=False, sort_keys=False,
                                              allow_unicode=True)
    path.write_text(content, encoding="utf-8")


def load_inbox() -> str:
    """Read inbox.md contents. Returns empty string if file doesn't exist."""
    path = _inbox_file()
    if not path.exists():
        return ""
    return path.read_text(encoding="utf-8")


