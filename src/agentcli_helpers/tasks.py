def init_task_files() -> dict[str, str]:
    """Bootstrap the tasks/ directory and three files. Idempotent.

    Returns a dict mapping file path -> action taken ('created' | 'exists').
    """
    tasks_dir = _resolve_tasks_dir()
    results: dict[str, str] = {}

    tasks_dir.mkdir(parents=True, exist_ok=True)

    # inbox.md
    inbox = _inbox_file()
    if not inbox.exists():
        inbox.write_text("", encoding="utf-8")
        results[str(inbox)] = "created"
    else:
        results[str(inbox)] = "exists"

# tasks.yaml
    tasks_file = _tasks_file()
    if not tasks_file.exists():
        data = {"meta": {"counter": 0}, "tasks": []}
        content = TASKS_HEADER + yaml.safe_dump(data, default_flow_style=False,
                                                 sort_keys=False, allow_unicode=True)
        tasks_file.write_text(content, encoding="utf-8")
        results[str(tasks_file)] = "created"
    else:
        # Validate existing file is parseable YAML
        try:
            load_tasks_yaml()
            results[str(tasks_file)] = "exists"
        except yaml.YAMLError:
            results[str(tasks_file)] = "invalid"
            return results

    # closed.yaml
    closed_file = _resolve_tasks_dir() / CLOSED_FILE_NAME
    if not closed_file.exists():
        # Check for old done.yaml to migrate
        old_done = _resolve_tasks_dir() / "done.yaml"
        if old_done.exists():
            # Rename old done.yaml to closed.yaml
            old_done.rename(closed_file)
            results[str(closed_file)] = "migrated from done.yaml"
        else:
            data = {"meta": {"total_closed": 0}, "tasks": []}
            content = CLOSED_HEADER + yaml.safe_dump(data, default_flow_style=False,
                                                      sort_keys=False, allow_unicode=True)
            closed_file.write_text(content, encoding="utf-8")
            results[str(closed_file)] = "created"
    else:
        results[str(closed_file)] = "exists"

    return results


def next_counter_and_id() -> tuple[int, str]:
    """Read meta.counter, increment it, save, and return (new_counter, formatted_id).

    Raises FileNotFoundError if tasks.yaml doesn't exist.
    """
    meta, tasks = load_tasks_yaml()
    counter = meta.get("counter", 0)
    if not isinstance(counter, int):
        counter = 0
    new_counter = counter + 1
    meta["counter"] = new_counter
    save_tasks_yaml(meta, tasks)
    return new_counter, _format_id(new_counter)


def add_task(title: str, priority: str | None = None, tags: list[str] | None = None,
             source: str | None = None, related: str | None = None,
             notes: str | None = None) -> dict[str, Any]:
    """Create a new task and prepend it to tasks.yaml.

    Returns the created task dict.
    """
    meta, tasks = load_tasks_yaml()
    counter = meta.get("counter", 0)
    if not isinstance(counter, int):
        counter = 0
    new_counter = counter + 1
    meta["counter"] = new_counter
    task_id = _format_id(new_counter)

    task: dict[str, Any] = {
        "id": task_id,
        "title": title,
        "status": "todo",
        "created": _now_date(),
        "closed": False,
    }
    if priority:
        task["priority"] = priority
    if tags:
        task["tags"] = [t.lower().strip().replace(" ", "-") for t in tags]
    if source:
        task["source"] = source
    if related:
        task["related"] = related
    if notes:
        # Store as list[str] — single string becomes one-element list
        task["notes"] = [notes]

    # Prepend: newest at top
    tasks.insert(0, task)
    save_tasks_yaml(meta, tasks)
    return task


def update_task(task_id: str, status: str | None = None, priority: str | None = None,
                tags: list[str] | None = None, related: str | None = None,
                notes: str | None = None, replace_notes: bool = False,
                closed: bool | None = None) -> dict[str, Any]:
    """Update fields on an existing task. Tags are appended, not replaced.

    Returns the updated task dict.
    Raises ValueError if task not found.
    """
    meta, tasks = load_tasks_yaml()
    task = _find_task_by_id(tasks, task_id)
    if task is None:
        raise ValueError(f"Task not found: {task_id}")

    if status is not None:
        task["status"] = status
    if priority is not None:
        task["priority"] = priority
    if tags is not None:
        existing = task.get("tags", []) or []
        new_tags = [t.lower().strip().replace(" ", "-") for t in tags]
        for tag in new_tags:
            if tag not in existing:
                existing.append(tag)
        task["tags"] = existing
    if related is not None:
        task["related"] = related
    if notes is not None:
        existing = _normalize_notes(task.get("notes"))
        if replace_notes or not existing:
            # --replace-notes or no existing notes → set fresh list
            task["notes"] = [notes]
        else:
            # Default: append to existing list
            existing.append(notes)
            task["notes"] = existing

    if closed is True:
        task["closed"] = True
        task["closed_at"] = _now_iso()
    elif closed is False:
        task["closed"] = False

    task["updated"] = _now_iso()

    if closed is True:
        # Move to closed.yaml
        tasks.remove(task)
        save_tasks_yaml(meta, tasks)
        closed_list = load_closed_yaml()
        closed_list.append(task)
        save_closed_yaml(closed_list)
    else:
        save_tasks_yaml(meta, tasks)

    return task


def close_task(task_id: str, note: str | None = None) -> dict[str, Any]:
    """Move a task from tasks.yaml to closed.yaml. Preserves its status.

    Sets closed=True, adds 'closed_at' timestamp.
    Appends optional --note to the existing notes field.
    Returns the closed task dict.
    Raises ValueError if task not found or already closed.
    """
    meta, tasks = load_tasks_yaml()
    task = _find_task_by_id(tasks, task_id)
    if task is None:
        # Check if task is already closed
        closed_list = load_closed_yaml()
        closed_task = _find_task_by_id(closed_list, task_id)
        if closed_task is not None:
            raise ValueError(f"Task already closed: {task_id}")
        raise ValueError(f"Task not found: {task_id}")

    # Remove from active list
    tasks.remove(task)

    # Update the task — PRESERVE status, set closed flag
    task["closed"] = True
    task["closed_at"] = _now_iso()
    task["updated"] = _now_iso()
    if note:
        existing_notes = _normalize_notes(task.get("notes"))
        existing_notes.append(note)
        task["notes"] = existing_notes

    # Save active list
    save_tasks_yaml(meta, tasks)

    # Append to closed.yaml (oldest-first: append to bottom)
    closed_list = load_closed_yaml()
    closed_list.append(task)
    save_closed_yaml(closed_list)

    return task


def filter_tasks(tasks: list[dict[str, Any]], status: str | None = None,
                 tags: list[str] | None = None, tag: str | None = None,
                 priority: str | None = None,
                 source: str | None = None, related: str | None = None) -> list[dict[str, Any]]:
    """Filter a task list by status, tags, priority, source, and/or related. All filters ANDed.

    tags: list of tags — task must contain ALL of them (AND logic).
    """
    result = tasks
    if status:
        result = [t for t in result if t.get("status") == status]
    if priority:
        result = [t for t in result if t.get("priority") == priority]
    if source:
        result = [t for t in result if t.get("source") == source]
    if tag:
        tags = (tags or []) + [tag]
    if tags:
        for tag in tags:
            result = [t for t in result if tag in (t.get("tags") or [])]
    if related:
        result = [t for t in result if t.get("related") == related]
    return result


