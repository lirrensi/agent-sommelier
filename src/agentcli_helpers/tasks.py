    if incoming:
        click.echo("\n  DEPENDED BY:")
        for dep_id, dep_type, title in incoming:
            click.echo(f"    {dep_id} ({dep_type}) — {title}")

    if not out_deps and not incoming:
        click.echo("  No dependencies.")
    click.echo("")


