"""Task management CLI for AI agents.

Statuses: todo, in-progress, done, blocked, postponed, cancelled,
          review, waiting, parked, deferred, backlog, abandoned

Usage:
    tasks init              # Bootstrap task files
    tasks next              # Show highest-priority todo
    tasks list              # List active tasks
    tasks show TSK-0001     # Full detail of one task
    tasks history           # Recently completed tasks
    tasks status            # Session overview
    tasks add "Title"       # Create a task
    tasks update TSK-0001   # Modify a task
    tasks close TSK-0001     # Close a task (any status)
    tasks inbox             # Print inbox contents
    tasks search <text>     # Full-text search across all tasks
"""

from __future__ import annotations

import os
import sys
from datetime import datetime
from pathlib import Path
from typing import Any

import click
import yaml
from rich.console import Console
from rich.table import Table

# ---------------------------------------------------------------------------
# Constants
# ---------------------------------------------------------------------------

TASKS_DIR_NAME = "tasks"
INBOX_FILE_NAME = "inbox.md"
TASKS_FILE_NAME = "tasks.yaml"
CLOSED_FILE_NAME = "closed.yaml"

VALID_STATUSES = {
    "todo", "in-progress", "done",           # core lifecycle
    "blocked", "postponed", "cancelled",      # deferral / termination
    "review", "waiting",                      # handoff / gate
    "parked", "deferred", "backlog",           # triage / queue
    "abandoned",                               # dead end
}
VALID_PRIORITIES = {"urgent", "high", "medium", "low"}
VALID_SOURCES = {"inbox", "audit", "test", "jira", "agent", "idea"}

PRIORITY_ORDER = {"urgent": 0, "high": 1, "medium": 2, "low": 3}

# ---------------------------------------------------------------------------
# File headers — written to the top of each YAML file when saved
# ---------------------------------------------------------------------------

TASKS_HEADER = """\
# ======================================================================
# AgentCLI Task System — Active Task List
# ======================================================================
#
# !! DO NOT EDIT THIS FILE DIRECTLY !!
# Use the `tasks` CLI tool to manage tasks.
# Manual edits will be overwritten. If the file is broken, run
# `tasks init` to repair it, then re-add missing tasks via the CLI.
#
# Commands:
#   tasks init              Bootstrap or repair task files
#   tasks add "Title"       Create a new task
#   tasks list              Show active tasks
#   tasks show TSK-NNNN     Full detail of one task
#   tasks update TSK-NNNN   Modify a task
#   tasks close TSK-NNNN    Close a task (any status)
#   tasks next              Highest-priority todo(s)
#   tasks status            Session overview
#   tasks history           Recently completed tasks
#   tasks inbox             Print inbox.md
#   tasks search <text>     Full-text search across all tasks
#   tasks --help            Full documentation
#
# Statuses:  todo, in-progress, done, blocked, postponed, cancelled,
#            review, waiting, parked, deferred, backlog, abandoned
# Closed:    tasks close TSK-NNNN  or  tasks update --closed
# Priority:  urgent > high > medium > low
# Newest at top, oldest at bottom.
# ======================================================================

"""

CLOSED_HEADER = """\
# ======================================================================
# AgentCLI Task System — Closed Task Archive
# ======================================================================
#
# !! DO NOT EDIT THIS FILE DIRECTLY !!
# Use `tasks close TSK-NNNN` to close a task. Run `tasks update --closed`
# to archive with a specific status. Manual edits will be overwritten.
#
# Append-only — nothing is ever deleted from this file.
# Newest at bottom, oldest at top (chronological).
#
# Run `tasks history` to view closed tasks.
# ======================================================================

"""


# ---------------------------------------------------------------------------
# Path resolution
# ---------------------------------------------------------------------------


def _resolve_tasks_dir() -> Path:
    """Find or default to the tasks/ directory relative to cwd."""
    cwd = Path.cwd()
    tasks_dir = cwd / TASKS_DIR_NAME
    return tasks_dir


def _tasks_file() -> Path:
    return _resolve_tasks_dir() / TASKS_FILE_NAME


def _inbox_file() -> Path:
    return _resolve_tasks_dir() / INBOX_FILE_NAME


# ---------------------------------------------------------------------------
# YAML file I/O
# ---------------------------------------------------------------------------


def load_tasks_yaml() -> tuple[dict[str, Any], list[dict[str, Any]]]:
    """Load tasks.yaml. Returns (meta, tasks_list).

    If file does not exist, raises FileNotFoundError.
    If YAML is malformed, raises yaml.YAMLError.
    If file exists but is empty or has unexpected structure, returns defaults.
    """
    path = _tasks_file()
    if not path.exists():
        raise FileNotFoundError(f"Tasks file not found: {path}\nRun 'tasks init' first.")

    raw = yaml.safe_load(path.read_text(encoding="utf-8")) or {}
    meta = raw.get("meta", {})
    if not isinstance(meta, dict):
        meta = {}
    tasks = raw.get("tasks", [])
    if not isinstance(tasks, list):
        tasks = []
    return meta, tasks


def save_tasks_yaml(meta: dict[str, Any], tasks: list[dict[str, Any]]) -> None:
    """Write tasks.yaml with meta and tasks list.

    Tasks are stored newest-first (by 'created' descending).
    Missing optional fields are excluded from output.
    """
    path = _tasks_file()
    path.parent.mkdir(parents=True, exist_ok=True)
    data: dict[str, Any] = {"meta": meta, "tasks": _strip_none_fields_from_list(tasks)}
    content = TASKS_HEADER + yaml.safe_dump(data, default_flow_style=False, sort_keys=False,
                                             allow_unicode=True)
    path.write_text(content, encoding="utf-8")


def load_closed_yaml() -> list[dict[str, Any]]:
    """Load closed.yaml. Returns list of closed tasks.

    Handles the {meta, tasks} format.
    Falls back to legacy done.yaml if closed.yaml doesn't exist.
    Returns [] if neither file exists.
    """
    path = _resolve_tasks_dir() / CLOSED_FILE_NAME
    if not path.exists():
        old_path = _resolve_tasks_dir() / "done.yaml"
        if old_path.exists():
            raw = yaml.safe_load(old_path.read_text(encoding="utf-8")) or []
            if isinstance(raw, dict):
                return raw.get("tasks", [])
            return raw if isinstance(raw, list) else []
        return []
    raw = yaml.safe_load(path.read_text(encoding="utf-8")) or []
    if isinstance(raw, dict):
        return raw.get("tasks", [])
    return raw if isinstance(raw, list) else []


def save_closed_yaml(closed_list: list[dict[str, Any]]) -> None:
    """Write closed.yaml with meta wrapper and header.

    Oldest-first (chronological order). Meta tracks total_closed counter.
    """
    path = _resolve_tasks_dir() / CLOSED_FILE_NAME
    path.parent.mkdir(parents=True, exist_ok=True)
    meta = {"total_closed": 0}
    if path.exists():
        existing_raw = yaml.safe_load(path.read_text(encoding="utf-8")) or {}
        if isinstance(existing_raw, dict):
            meta = existing_raw.get("meta", meta)
    meta["total_closed"] = len(closed_list)
    data = {"meta": meta, "tasks": _strip_none_fields_from_list(closed_list)}
    content = CLOSED_HEADER + yaml.safe_dump(data, default_flow_style=False, sort_keys=False,
                                              allow_unicode=True)
    path.write_text(content, encoding="utf-8")


def load_inbox() -> str:
    """Read inbox.md contents. Returns empty string if file doesn't exist."""
    path = _inbox_file()
    if not path.exists():
        return ""
    return path.read_text(encoding="utf-8")


