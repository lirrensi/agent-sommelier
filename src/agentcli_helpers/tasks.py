    incoming: list[tuple[str, str, str]] = []  # (dep_id, dep_type, title)
    for t in tasks + closed_list:
        for dep in (t.get("deps") or []):
            if isinstance(dep, dict) and dep.get("id") == task_id:
                incoming.append((t.get("id", "?"), dep.get("type", "?"), t.get("title", "?")))
